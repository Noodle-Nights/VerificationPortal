app.listen(PORT, () => {
  console.log(`\n  Secure ID Verification Platform`);
  console.log(`  http://localhost:${PORT}\n`);
  if (!discordConfigured()) {
    console.warn('  [WARN] Discord OAuth is not fully configured — see .env.example');
  }
  if (!process.env.SESSION_SECRET) {
    console.warn('  [WARN] SESSION_SECRET not set — sessions reset on every restart');
    console.warn('         Run:  npm run secret\n');
  }
});
